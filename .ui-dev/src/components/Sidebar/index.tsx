// internal imports
import React, { useCallback } from 'react';
import { IconFolderPlus, IconMistOff, IconPlus } from '@tabler/icons-react';

// internal imports
import CloseSidebarButton from './components/CloseSidebarButton';
import OpenSidebarButton from './components/OpenSidebarButton';
import Search from '../Search';

interface ISidebarProps {
  isOpen: boolean;
  addItemButtonTitle: string;
  side: 'left' | 'right';
  items: any[];
  itemComponent: React.ReactNode;
  folderComponent: React.ReactNode;
  footerComponent?: React.ReactNode;
  searchTerm: string;
  handleSearchTerm: (searchTerm: string) => void;
  toggleOpen: () => void;
  handleCreateItem: () => void;
  handleCreateFolder: () => void;
}

const Sidebar: React.FC<ISidebarProps> = ({
  isOpen,
  addItemButtonTitle,
  side,
  items,
  itemComponent,
  folderComponent,
  footerComponent,
  searchTerm,
  handleSearchTerm,
  toggleOpen,
  handleCreateItem,
  handleCreateFolder,
}) => {
  const allowDrop = useCallback((e: any) => {
    e.preventDefault();
  }, []);

  const highlightDrop = useCallback((e: any) => {
    e.target.style.background = '#343541';
  }, []);

  const removeHighlight = useCallback((e: any) => {
    e.target.style.background = 'none';
  }, []);

  const renderSearchButton = useCallback(() => {
    if (!items.length) {
      return null;
    }

    return (
      <Search
        placeholder={'Search...'}
        searchTerm={searchTerm}
        onSearch={handleSearchTerm}
      />
    );
  }, [handleSearchTerm, items.length, searchTerm]);

  return isOpen ? (
    <div>
      <div
        className={`fixed top-0 ${side}-0 z-40 flex h-full w-[260px] flex-none flex-col space-y-2 bg-[#202123] p-2 text-[14px] transition-all sm:relative sm:top-0`}
      >
        <div className="flex items-center">
          <button
            className="text-sidebar flex w-[190px] flex-shrink-0 cursor-pointer select-none items-center gap-3 rounded-md border border-white/20 p-3 text-white transition-colors duration-200 hover:bg-gray-500/10"
            onClick={() => {
              handleCreateItem();
              handleSearchTerm('');
            }}
          >
            <IconPlus size={16} />
            {addItemButtonTitle}
          </button>

          <button
            className="ml-2 flex flex-shrink-0 cursor-pointer items-center gap-3 rounded-md border border-white/20 p-3 text-sm text-white transition-colors duration-200 hover:bg-gray-500/10"
            onClick={handleCreateFolder}
          >
            <IconFolderPlus size={16} />
          </button>
        </div>

        {renderSearchButton()}

        <div className="flex-grow overflow-auto">
          {items?.length > 0 && (
            <div className="flex border-b border-white/20 pb-2">
              {folderComponent}
            </div>
          )}

          {items?.length > 0 ? (
            <div
              className="pt-2"
              onDragOver={allowDrop}
              onDragEnter={highlightDrop}
              onDragLeave={removeHighlight}
            >
              {itemComponent}
            </div>
          ) : (
            <div className="mt-8 select-none text-center text-white opacity-50">
              <IconMistOff className="mx-auto mb-3" />
              <span className="text-[14px] leading-normal">
                {'No data.'}
              </span>
            </div>
          )}
        </div>
        {footerComponent}
      </div>

      <CloseSidebarButton onClick={toggleOpen} side={side} />
    </div>
  ) : (
    <OpenSidebarButton onClick={toggleOpen} side={side} />
  );
};

export default Sidebar;
